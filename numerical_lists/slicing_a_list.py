# Slicing a list

# allows output of any name on list
names = ['nadia','salma','nabeelah','saira']
print(names[0:2])

# to print the first to 3rd name in a list
# 0:3 meaans 0-4 but 4th name is omitted
print(names[0:3])

# to print from the 2nd name in a list up to the last
print(names[2:])

# to print up to the 2nd name in a list
print(names[:2])

# to print the last 3 names in a list
print(names[-3:])

print(names[1:2])